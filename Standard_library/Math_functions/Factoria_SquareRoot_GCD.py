import math

# factorial
print(math.factorial(3))
print(math.sqrt(64))


'''GCD - stands for Greates common denominator
useful fro reducing fractions
-8/52 -> 2/13
GCD between 8 and 52 is 4, so we can divide the numerator and the denominator by 4 to get a reduction fraction
'''
print(math.gcd(52, 8))
print(math.gcd(8,52))
print(8/52)
print(2/13)

# Degrees and Radians
print(math.radians(360))
print(math.pi * 2)
print(math.degrees(math.pi * 2))
