''' What to know - Given 0, 5, 8
Min
lowest item is 0
Max
greatest item is 8
'''
playerOneScore = 10
plaTwoScore = 4
print(min(playerOneScore, plaTwoScore)) # 4

print(min(0, 12, -19)) #-19

print(min("Kathryn", "Katie")) # Kathryn because h come before y in the alphabet
print(min("Angela", "Bob")) # Angela because B comes after a in the alphabet

print(max(playerOneScore, plaTwoScore))
playerThreeScore = 14
print(max(playerOneScore, plaTwoScore, playerThreeScore))
print(max("Kathryn", "Katie"))